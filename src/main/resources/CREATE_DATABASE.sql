--- 2. CREATION DES TABLES
---------------------------------
DROP TABLE USERNAME;
 CREATE TABLE USERNAME(

  ID_USER 	NUMBER(10),
  NOM    	VARCHAR(255),
  PRENOM	VARCHAR(50),
  PSEUDO    VARCHAR(50), 
   PRIMARY KEY (ID_USER)
); 

DROP TABLE FORMAT_TOURNOI;
 CREATE TABLE FORMAT_TOURNOI(

  ID_FORMAT 		NUMBER(10),
  NOM_FORMAT 		VARCHAR(50),
  NB_CARTES	    	NUMBER(3),
  COMMENTAIRES    	VARCHAR(50), 
   PRIMARY KEY (ID_FORMAT)
); 

DROP TABLE TOURNOI;
CREATE TABLE TOURNOI(

	ID_TOURNOI	    NUMBER(10),
	ID_FORMAT	    NUMBER(10),
    NOM_TOURNOI		VARCHAR(80)
	
	PRIMARY KEY (ID_TOURNOI),
	FOREIGN KEY (ID_FORMAT)
		REFERENCES FORMAT_TOURNOI(ID_FORMAT)

);

DROP TABLE APPARIEMENT;
CREATE TABLE APPARIEMENT (

	ID_APPARIEMENT	NUMBER(10),
	ID_TOURNOI	NUMBER(10),
	ID_USER1		NUMBER(10),
	ID_USER2		NUMBER(10),
	NO_RONDE 		NUMBER(3),
	RESULTAT1		NUMBER(2),
	RESULTAT2		NUMBER(2),

	PRIMARY KEY (ID_APPARIEMENT),
	FOREIGN KEY (ID_TOURNOI)
		REFERENCES TOURNOI(ID_TOURNOI),
	FOREIGN KEY (ID_USER1)
		REFERENCES USERNAME(ID_USER),
	FOREIGN KEY (ID_USER2)
		REFERENCES USERNAME(ID_USER)

);

DROP TABLE RESULTAT;
CREATE TABLE RESULTAT (

	ID_TOURNOI	NUMBER(10),
	ID_USER		NUMBER(10),
	SCORE		NUMBER(10),
	RANG 		NUMBER(3),
	ROUND       NUMBER(3),

	PRIMARY KEY (ID_TOURNOI,ID_USER)
